def convert_to_style(text, style):
    return ''.join(styles[style].get(char, char) for char in text)

# Prompt the user for input
input_text = input("Enter text to convert: ")

# Present style options
print("Choose a style:")
for i, style in enumerate(styles.keys(), 1):
    print(f"{i}. {style}")

# Get the user's choice
choice = int(input("Enter the number of the style: "))
selected_style = list(styles.keys())[choice - 1]

# Convert and print the result
converted_text = convert_to_style(input_text, selected_style)
print(f"{selected_style} text:", converted_text)

# Copy the result to clipboard using wl-copy
subprocess.run(['wl-copy'], input=converted_text, text=True)